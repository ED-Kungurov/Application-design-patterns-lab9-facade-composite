public class LightingSystem {
    void on(){
        System.out.println("LightingSystem is on");
    }
    void setLightningLevel(int level){
        System.out.println("LightingSystem set lightning level on " + level + "%");
    }
    void off(){
        System.out.println("LightingSystem is off");
    }
    void diskotekaMode(){
        System.out.println("LightingSystem is in diskoteka mode");
    }
    void diagnostic(){
        System.out.println("LightingSystem is ok");
    }
}
